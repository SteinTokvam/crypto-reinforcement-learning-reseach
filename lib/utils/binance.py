class Binance:
    def buy(self, current_price, amount_to_buy): # kan brukes for å håndtere kjøp mot f.eks binance
        print(f"Kjøper {amount_to_buy} crypto på pris {current_price}")
        

    def sell(self, current_price, amount_to_sell): # kan brukes for åndtere salg mot f.eks binance
        print(f"Selger {amount_to_sell} crypto på pris {current_price}") 
        